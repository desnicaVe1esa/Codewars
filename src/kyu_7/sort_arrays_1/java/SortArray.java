package kyu_7.sort_arrays_1.java;

import java.util.Arrays;

public class SortArray{
    public static Object[] sortArray(Object[] names){
        Arrays.sort(names);
        return names;
    }
    public static long[] sortArray(long[] names){
        Arrays.sort(names);
        return names;
    }
    public static int[] sortArray(int[] names){
        Arrays.sort(names);
        return names;
    }
    public static double[] sortArray(double[] names){
        Arrays.sort(names);
        return names;
    }
    public static float[] sortArray(float[] names){
        Arrays.sort(names);
        return names;
    }
}
